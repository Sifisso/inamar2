package mz.ciuem.inamar.entity.report;

public class PeticaoDelegacao {
	
	private String admar;
	private String area;
	private String tipoPeticao;
	private String terminada;
	private String quantidade;
	public String getAdmar() {
		return admar;
	}
	public void setAdmar(String admar) {
		this.admar = admar;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getTipoPeticao() {
		return tipoPeticao;
	}
	public void setTipoPeticao(String tipoPeticao) {
		this.tipoPeticao = tipoPeticao;
	}
	
	public String getTerminada() {
		return terminada;
	}
	public void setTerminada(String terminada) {
		this.terminada = terminada;
	}
	public String getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(String quantidade) {
		this.quantidade = quantidade;
	}
	
}
